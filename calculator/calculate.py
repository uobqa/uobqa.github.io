
def calcOperation(inumOne, inumTwo, numOp):
    numOne = int(inumOne)
    numTwo = int(inumTwo)
    if numOp == '+':
        result = numOne + numTwo
        print(f'{numOne} + {numTwo} is {result}')

    if numOp == '-':
            result = numOne - numTwo
            print(f'{numOne} - {numTwo} is {result}')

    if numOp == '*':
            result = numOne * numTwo
            print(f'{numOne} * {numTwo} is {result}')

    if numOp == '/':
            result = numOne / numTwo
            print(f'{numOne} / {numTwo} is {result}')

  



